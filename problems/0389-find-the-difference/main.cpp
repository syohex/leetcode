#include <cassert>
#include <string>
#include <map>

char findTheDifference(const std::string &s, const std::string &t) {
    std::map<char, int> sm, tm;
    for (size_t i = 0; i < t.size(); ++i) {
        ++tm[t[i]];
        if (i != t.size() - 1) {
            ++sm[s[i]];
        }
    }

    for (const auto &it : tm) {
        if (sm.find(it.first) == sm.end() || sm[it.first] != it.second) {
            return it.first;
        }
    }

    return '\0';
}

